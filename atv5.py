altura = float(input("Digite a altura da caixa d'água: "))
largura = float(input("Digite a largura da caixa d'água: "))
profundidade = float(input("Digite a profundidade da caixa d'água: "))

volume = altura * largura * profundidade

print(f"o Volume da caixa d'água é: {volume}")
